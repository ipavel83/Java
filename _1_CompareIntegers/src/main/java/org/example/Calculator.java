package org.example;

public class Calculator {
    static int summ(int a, int b) {
        return a + b;
    }

    static int diff(int a, int b) {
        return a - b;
    }

    static int div(int a, int b) {
        return a / b;
    }

    static long mul(int a, int b) {
        return (long)a * (long)b;
    }
}
